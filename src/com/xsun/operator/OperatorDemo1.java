package com.xsun.operator;

public class OperatorDemo1 {
    static void main(String[] args) {
        // + , - , * , / , % 的运算


        //整数
        int a = 10;
        int b = 3;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        System.out.println("-------------------------");

        //浮点数
        double c =10;
        double d = 3;
        System.out.println(c + d);
        System.out.println(c - d);
        System.out.println(c * d);
        System.out.println(c / d);
        System.out.println(c % d);
        System.out.println("-----------------------------");

        float e =3;
        System.out.println(c + e);
        System.out.println(c - e);
        System.out.println(c * e);
        System.out.println(c / e);
        System.out.println(c % e);


    }
}
